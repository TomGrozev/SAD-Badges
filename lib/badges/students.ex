defmodule Badges.Students do
  @moduledoc """
  The Students context.
  """

  import Ecto.Query, warn: false
  alias Badges.Repo

  alias Badges.Students.Student

  @doc """
  Returns the list of students.

  ## Examples

      iex> list_students()
      [%Student{}, ...]

  """
  def list_students do
    Repo.all(from s in Student, preload: [:group])
  end

  @doc """
  Search for students

  Can search by first_name and/or last_name
  """
  def search_students(query) do
    where(Student,
      fragment(
        "to_tsvector('english', first_name || ' ' || coalesce(last_name, ' ')) @@ to_tsquery(?)",
        ^prefix_search(query)
      )
    )
    |> preload([:group])
    |> select([:id, :first_name, :last_name, :group_id])
    |> Repo.all()
  end

  defp prefix_search(term), do: String.replace(term, ~r/\W/u, "") <> ":*"

  @doc """
  Gets a single student.

  Raises `Ecto.NoResultsError` if the Student does not exist.

  ## Examples

      iex> get_student!(123)
      %Student{}

      iex> get_student!(456)
      ** (Ecto.NoResultsError)

  """
  def get_student!(id) do
    Repo.get!(Student, id)
    |> Repo.preload(:group)
  end

  @doc """
  Creates a student.

  ## Examples

      iex> create_student(%{field: value})
      {:ok, %Student{}}

      iex> create_student(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_student(attrs \\ %{}) do
    %Student{}
    |> Student.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a student.

  ## Examples

      iex> update_student(student, %{field: new_value})
      {:ok, %Student{}}

      iex> update_student(student, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_student(%Student{} = student, attrs) do
    student
    |> Student.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a student.

  ## Examples

      iex> delete_student(student)
      {:ok, %Student{}}

      iex> delete_student(student)
      {:error, %Ecto.Changeset{}}

  """
  def delete_student(%Student{} = student) do
    Repo.delete(student)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking student changes.

  ## Examples

      iex> change_student(student)
      %Ecto.Changeset{data: %Student{}}

  """
  def change_student(%Student{} = student, attrs \\ %{}) do
    Student.changeset(student, attrs)
  end

